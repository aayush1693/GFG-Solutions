class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int sum=0;
        int num=n;
        while(n!=0){
            int digit=n%10;
            sum=sum+pow(digit,3);
            n=n/10;
        }
        return num==sum? "Yes": "No";
    }
};

